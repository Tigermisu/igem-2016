        <p>The first attempt to build our BioBrick™ consisted in the assembly of eight different parts. We transformed E. coli
            with each of our BioBricks™ to increase the quantity of DNA we had, then we extracted the plasmid of each one
            of them so we could make the assembly. Each plasmid that was extracted was ran in an electrophoresis gel so we
            could be certain that what we obtained was what we wanted. </p>
        <p>When we did the last electrophoresis we had as a result that all the parts that we extracted were right, with the
            exception of one of them, we believe that the chloramphenicol backbone was supercoiled and that’s the reason
            it appeared to be at a higher molecular weight. </p>
        <img src="http://2016.igem.org/wiki/images/0/0a/T--Tec-Chihuahua--Electroforesis.jpg" alt="" class="img-responsive center-block margin">
        <p>After this we quantified how much nanograms of DNA we had per microliter and proceeded to do the 3A assembly. Since not everything is digested and ligated properly during the assembly, there are some backbones without the insert that can interfere. Because of this there were two types of colonies in the plates where our transformed cells were plated: pink and white. The ones that we were interested in were the white ones, since these are the ones that have what we want correctly inserted; the pink ones are the backbones that were not correctly digested and ligated and thus are of no use for us. We grab one of the white colonies and put it to grow in LB broth, so we could later extract its plasmid and start this process again until we have the BioBrick™ we desire.</p>

