        <h2>Confrontations</h2>
        <p>The inhibitory activity of our isolated <i>Myxobacteria</i> was proven by confronting them with previously isolated
            fungus. </p>
        <p>However, our project needed our <i>Myxobacteria</i> to be confronted with fungus isolated from crops. Our
                    main objective was to test its action against <i>R. solani</i>, and see if <i>Myxobacteria</i> had inhibitory
                    action against it. Confrontations were performed in PDA agar plates, Myxococcus xanthus against Rhizoctonia
                    solani and later against <i>Rhizopus oryzae</i>. It was determined that unmodified <i>Myxobacteria</i> are able
                    to inhibit fungal growth. A major inhibition was observed in <i>Rhizoctonia</i> than in <i>Rhizopus</i>. Other confrontations
                    were performed against uncharacterized fungi only to challenge Myxobacteria. </p>
