        <h2>Biological Safety Cabinets</h2>
        <p>In the lab we use biological safety cabinets in order to prevent us to get in contact with the samples that are being analyzed as well as protecting them from getting contaminated. 
Every time we make use of the biological safety cabinets, we first sterilize it by leaving the UV light on for 15 minutes with the material that's going to be used in the protocols, we also disinfect our hands with 70% ethanol before getting them into the cabinet, and clean it every time we finish using it. </p>
