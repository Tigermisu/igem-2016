        <h2>Wet Lab: Decontamination and Waste Management</h2>
        <p>Waste must be managed in different ways depending on what is going to be disposed. Sharp objects must be disposed in a red thick plastic bottle, biological wastes such as fluids must be disposed in a yellow hermetic bottle, solid biological wastes in a yellow bag, and objects that have been in contact with bacteria or other infectious agents should be disposed in a red bag.</p>
        <p>We worked on a Containment Level 2 Laboratory, with lockable doors and previous training to handle materials, equipments and biosafety. Benches are covered in varnish (non-absorbent material). The access to the laboratory is restricted to Biotechnology alumni and our Faculty teachers.</p>
