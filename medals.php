        <h2>Silver</h2>
        <img src="http://parts.igem.org/wiki/images/thumb/b/b0/T--Tec-Chihuahua--Silvermedal.jpg/614px-T--Tec-Chihuahua--Silvermedal.jpg.png" alt="Silver Medal Icon" class="img-responsive center-block" style="max-height: 200px;">
        
        <ul class="big-list">
            <li><strong>1.- Validated part/validated contribution:</strong> We have validated the biobrick part <a href="http://parts.igem.org/Part:BBa_K2069007">BBa_K2069007</a>                through our experiments carried out with a sensor that allowed us to measure light reflection on a very sensitive
                level, developing data that was implemented in a mathematical model used to make a direct relationship with
                the expression of an anti-fungal protein in myxobacteria, which is the cell used as chassis for the experiments.
                In this way, we could make predictions and adjustments based on the risk level that protein over expression
                could lead to. <a href="http://2016.igem.org/Team:Tec-Chihuahua/projectparts">Please see our page for more information</a>.
            </li>
            <li><strong>2.- Collaboration:</strong> <a href="http://2016.igem.org/Team:Tec-Chihuahua/Collaborations">See our collaboration page to know more about the teams we had the opportunity to collaborate with</a>.</li>
            <li><strong>3.- Human practices:</strong> Our team is really excited and proud of what we have accomplished in the
                Human Practices area. We started by developing an analysis where synthetic biology was studied in three aspects:
                economic, legal and ethical. From every approach, we encountered several issues like openness, democracy,
                IP conflict, winner takes all economy, social responsibility, biosecurity and involuntary risks. Each of
                these problematics was addressed with a possible solution or explanation and was considered in the creation
                of nine guidelines that should be implemented when regulating or even working with synthetic biology. Nevertheless
                we did not stop there, the team decided to develop activities that served as practical solutions to some
                of the issues as well as a confirmation to what had already been researched and analyzed. <br> Some of the
                problems we addressed were: the lack of information and diffusion, the farmers’ feeling of exclusion towards
                biotechnology/synthetic biology and the limited visualization of the impact of our project. Here we have
                enlisted the activities that helped us: <br>
                <ul>
                    <li>We created an event at the facilities of our college called “Café Con Ciencia: Myxobacteria as biological
                        control method for phytopathogenic fungi in crops” where we exposed to alumni, professors and general
                        public what our project was about.</li>
                    <li>With the objective of increasing dialogue, diffusion and public discussion we interviewed and exchanged
                        information with several farmers in our region. We explained what biotechnology and synthetic biology
                        are about and what we intended to do with our project, while the farmers gave us data of their crops,
                        taught us about the problems they normally face at agriculture and their personal points of view
                        of our project.</li>
                    <li>The team decided to address the farmer´s feeling of exclusion by organizing several visits to their farms.
                        This helped us realize their problems in flesh and visualize a broader picture of what we could do
                        in a future with our project and synthetic biology. <a href="http://2016.igem.org/Team:Tec-Chihuahua/HP/Silver">For more information, please see our HP page</a>.</li>
                    <li>
                        As part of our intent to increase our diffusion we participated and collaborated in the Guanajuato Meet-up. This event consisted in the gathering of several Mexican iGem teams, where we had the chance to spread the word about our project, reaching people out of our region. For more information, <a href="http://2016.igem.org/Team:Tec-Chihuahua/HP/Silver">please see our human practices page.</a>
                    </li>
                </ul>
            </li>
